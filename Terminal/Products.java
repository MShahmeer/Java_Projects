package com.oop.lab.terminal;

/**
 *
 * @author Muhammad Shameer
 */
public class Products {
    
    private int id;
    private String name;
    private double price;

    public Products(int id ,String name, double price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }

    public int getId() {
        return id;
    }
    
}
